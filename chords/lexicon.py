class Strict(Lexicon):

    def __init__(self, vocab_dim=157):
        Lexicon.__init__(self, vocab_dim=vocab_dim)
        self.valid_qualities = L.QUALITIES[vocab_dim]

    def __store_label__(self, label):
        try:
            row = L.split(label)
        except mir_eval.chord.InvalidChordException:
            row = ['X', '', set(), '']
        skip = [row[0] == 'X',
                not row[1] in self.valid_qualities,
                len(row[2]) > 0,
                not row[3] in ['', '1']]
        if any(skip):
            idx = None
        elif row[0] == 'N':
            idx = self.vocab_dim - 1
        else:
            idx = mir_eval.chord.pitch_class_to_semitone(row[0])
            idx += self.valid_qualities.index(row[1]) * 12
        self._index_map[label] = idx

    def __store_index__(self, index):
        if index < 0 or index >= self.vocab_dim:
            raise ValueError("index out of bounds: %d" % index)
        if index == self.vocab_dim - 1:
            label = "N"
        else:
            label = "%s:%s" % (L.ROOTS[index % 12],
                               L.QUALITIES[self.vocab_dim][int(index) / 12])
        self._label_map[index] = label


def chord_label_to_class_index_soft(label, vocab_dim=157):
    """Map a chord label to its class index, or None if undefined.

    Parameters
    ----------
    label : str or array_like
        Chord label(s) to map.
    vocab_dim : int
        Number of chords in the vocabulary.
    """
    N_quality_idx = (vocab_dim - 1) / 12
    singleton = False
    if isinstance(label, str):
        label = [label]
        singleton = True
    root, semitones, bass = mir_eval.chord.encode_many(label)
    quality_idx = [L.semitones_index(s, vocab_dim) for s in semitones]
    class_idx = []
    for r, q in zip(root, quality_idx):
        if N_quality_idx == q:
            idx = vocab_dim - 1
        else:
            idx = None if None in [q, r] else r + q * 12

        class_idx.append(idx)
    # class_idx[N_quality_idx == quality_idx] = vocab_dim - 1
    return class_idx[0] if singleton else class_idx


class StrictBigram(Strict):

    def __init__(self):
        Strict.__init__(self, vocab_dim=157)
        self._bigram_index_map = dict()
        self._bigram_tuple_map = dict()

        Nq = len(__bigrams__)
        for bigram in __bigrams__:
            a, b = bigram['label']
            for n in range(12):
                a_q = L.add_mod(a, n, 12) if a != self.vocab_dim - 1 else a
                b_q = L.add_mod(b, n, 12) if b != self.vocab_dim - 1 else b
                abs_idx = n*Nq + bigram['index']
                if not b_q in self._bigram_tuple_map:
                    self._bigram_tuple_map[b_q] = dict()
                self._bigram_tuple_map[b_q][a_q] = abs_idx

                # Here, all bigram states are mapped to labels; currently
                #   unclear if this is a good or bad idea.
                chord_label = Strict.index_to_label(self, b_q)
                self._bigram_index_map[abs_idx] = chord_label
        self._bigram_tuple_map[156][156] = 12*Nq
        self._bigram_index_map[12*Nq] = 'N'
        self.num_classes = 12*Nq + 1

    def label_to_index(self, bigram):
        """Index bigram label.

        Parameters
        ----------
        bigram : tuple, or array_like
            Chord birgram(s) to map.
        """
        singleton = False
        if len(np.asarray(bigram).flatten()) == 2:
            bigram = [bigram]
            singleton = True
        bigram = np.asarray(bigram)
        a_idx = Strict.label_to_index(self, bigram[:, 0])
        b_idx = Strict.label_to_index(self, bigram[:, 1])
        state_idx = np.array([self._bigram_tuple_map.get(b, {}).get(a, None)
                              for b, a in zip(b_idx, a_idx)])
        return state_idx[0] if singleton else state_idx

    def index_to_label(self, index):
        singleton = False
        if isinstance(index, int):
            index = [index]
            singleton = True

        chord_labels = [self._bigram_index_map.get(idx, "X") for idx in index]
        return chord_labels[0] if singleton else chord_labels


